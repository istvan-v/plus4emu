#ifndef P4FLICONV_MCCHAR_HPP
#define P4FLICONV_MCCHAR_HPP

#include "p4fliconv.hpp"

namespace Plus4FLIConv {

  class P4FLI_MultiColorChar : public FLIConverter {
   public:
    class Line128 {
     private:
      float   *buf;
     public:
      Line128();
      Line128(const Line128& r);
      virtual ~Line128();
      Line128& operator=(const Line128& r);
      void clear();
      void setBorderColor(float c);
      inline float& operator[](long n)
      {
        return buf[n + 16L];
      }
      inline float& pixel(long x)
      {
        return buf[x + 16L];
      }
      inline float getPixel(long x) const
      {
        return buf[x + 16L];
      }
      inline void setPixel(long x, float n)
      {
        if (x >= 0L && x < 128L)
          buf[x + 16L] = n;
      }
    };
    // ------------------------
    class Image128x64 {
     private:
      Line128 *buf;
     public:
      Image128x64();
      Image128x64(const Image128x64& r);
      virtual ~Image128x64();
      Image128x64& operator=(const Image128x64& r);
      inline Line128& operator[](long n)
      {
        return buf[n];
      }
    };
    // ------------------------
    double  monitorGamma;
    double  ditherLimit;
    double  ditherScale;
    int     ditherMode;
    bool    luminance1BitMode;
   private:
    Image128x64 resizedImage;
    Image128x64 ditherErrorImage;
    int     *ditheredImage;
    int     colorTable[4];
    float   yTable[9];
    // ----------------
    static void pixelStoreCallback(void *, int, int, float, float, float);
    void checkParameters();
    void ditherLine(long yc);
   public:
    P4FLI_MultiColorChar();
    virtual ~P4FLI_MultiColorChar();
    virtual bool processImage(PRGData& prgData, unsigned int& prgEndAddr,
                              const char *infileName,
                              YUVImageConverter& imgConv,
                              Plus4Emu::ConfigurationDB& config);
  };

}       // namespace Plus4FLIConv

#endif  // P4FLICONV_MCCHAR_HPP

